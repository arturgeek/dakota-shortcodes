<h1>Dakota Shotcodes</h1>
<p>
    The main puporse of this set of shortcodes is to facilitate a way to implement common requirements from different clients 
    when we are building their eCommerce sites, this in order to improve the time implementing the solutions as well as adding value 
    with an Out of The Box enhancer to woocommerce. 
</p>
<div>
    <h2>[dakota_cart_count]</h2>
    <p>
        This shortcode will present a Woocommerce Cart Count you can customize:
        <ul>
            <li>
                <b>bgcolor:</b> Background color for the cart SVG image
            </li>
            <li>
                <b>fcolor:</b> Font color for the item count
            </li>
        </ul> 
        <code>
        [dakota_cart_count bgcolor="#000000" fcolor="#FFFFFF" /]
        </code>
    </p>
</div>
<div>
    <h2>[dakota_account_link]</h2>
    <p>
        This shortcode will present a link which will redirect to My Account page defined in WooCommerce
        <code>
        [dakota_account_link /]
        </code>
    </p>
</div>